export const ALL_VENDORS = ['Bambu Lab', ...Object.keys(VENDOR_CATALOGUES)];

// Get colours for a vendor+material combo
export function getVendorColors(vendor, material) {
  if (vendor === 'Bambu Lab') return null; // handled by bambuCatalogue.js
  const cat = VENDOR_CATALOGUES[vendor];
  if (!cat) return [];
  return cat.colors;
}

// Get price for vendor+material
export function getVendorPrice(vendor, material) {
  if (vendor === 'Bambu Lab') return null;
  const cat = VENDOR_CATALOGUES[vendor];
  if (!cat) return null;
  return cat.materials[material] || null;
}

// Get materials available for a vendor
export function getVendorMaterials(vendor) {
  if (vendor === 'Bambu Lab') return null;
  const cat = VENDOR_CATALOGUES[vendor];
  if (!cat) return [];
  return Object.keys(cat.materials);
}
